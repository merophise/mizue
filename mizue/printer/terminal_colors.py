from typing import Final


class TerminalColors:
    ENDC: Final[str] = '\033[00m'
    SUCCESS: Final[str] = '\033[92m'
    WARNING: Final[str] = '\033[93m'
    ERROR: Final[str] = '\033[91m'
    INFO: Final[str] = '\033[94m'
    UNDERLINE: Final[str] = '\033[4m'
    BOLD: Final[str] = '\u001b[1m'
    BLACK: Final[str] = '\u001b[30m'
    RED: Final[str] = '\u001b[31m'
    GREEN: Final[str] = '\u001b[32m'
    YELLOW: Final[str] = '\u001b[33m'
    BLUE: Final[str] = '\u001b[34m'
    MAGENTA: Final[str] = '\u001b[35m'
    CYAN: Final[str] = '\u001b[36m'
    WHITE: Final[str] = '\u001b[37m'
    BRIGHT_BLACK: Final[str] = '\u001b[30;1m'
    BRIGHT_RED: Final[str] = '\u001b[31;1m'
    BRIGHT_GREEN: Final[str] = '\u001b[32;1m'
    BRIGHT_YELLOW: Final[str] = '\u001b[33;1m'
    BRIGHT_BLUE: Final[str] = '\u001b[34;1m'
    BRIGHT_MAGENTA: Final[str] = '\u001b[35;1m'
    BRIGHT_CYAN: Final[str] = '\u001b[36;1m'
    BRIGHT_WHITE: Final[str] = '\u001b[37;1m'
    BG_BLACK: Final[str] = '\u001b[40m'
    BG_RED: Final[str] = '\u001b[41m'
    BG_GREEN: Final[str] = '\u001b[42m'
    BG_YELLOW: Final[str] = '\u001b[43m'
    BG_BLUE: Final[str] = '\u001b[44m'
    BG_MAGENTA: Final[str] = '\u001b[45m'
    BG_CYAN: Final[str] = '\u001b[46m'
    BG_WHITE: Final[str] = '\u001b[47m'
    BG_BRIGHT_BLACK: Final[str] = '\u001b[40;1m'
    BG_BRIGHT_RED: Final[str] = '\u001b[41;1m'
    BG_BRIGHT_GREEN: Final[str] = '\u001b[42;1m'
    BG_BRIGHT_YELLOW: Final[str] = '\u001b[43;1m'
    BG_BRIGHT_BLUE: Final[str] = '\u001b[44;1m'
    BG_BRIGHT_MAGENTA: Final[str] = '\u001b[45;1m'
    BG_BRIGHT_CYAN: Final[str] = '\u001b[46;1m'
    BG_BRIGHT_WHITE: Final[str] = '\u001b[47;1m'
    RESET: Final[str] = '\u001b[0m'
